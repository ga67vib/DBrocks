package de.tum.in.dbpra.model.bean;

public class HasTimeBean {
	private int bindID;
	private int dateID;
	
	
	public HasTimeBean() {}


	/**
	 * @return the bindID
	 */
	public int getBindID() {
		return bindID;
	}


	/**
	 * @param bindID the bindID to set
	 */
	public void setBindID(int bindID) {
		this.bindID = bindID;
	}


	/**
	 * @return the dateID
	 */
	public int getDateID() {
		return dateID;
	}


	/**
	 * @param dateID the dateID to set
	 */
	public void setDateID(int dateID) {
		this.dateID = dateID;
	}


	
}
